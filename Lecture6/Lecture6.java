import java.lang.Math;

public class Lecture6 {
    public static void main (String args[]) {

        double x = -2.4;

        System.out.println(Math.abs(x)); // the result is 2.4

        System.out.println(Math.pow(2, 2)); // the result is 4

        System.out.println(Math.pow(2, -2)); // the result is 0.25

        System.out.println(Math.pow(-2, 2)); // the result is 4

        System.out.println(Math.pow(-2, -2)); // the result is 0.25

        System.out.println(Math.pow(2, 2.5)); // the result is 5.6569

        System.out.println(Math.sqrt(2)); // the result is 1.41

        
    }
}
